const filterCandlestickData = (data) => {
  // var xArr = []
  var rsiArr = [] // rsi指标数据
  var lbArr = [] // lb 指标
  var arr = []// 存储蜡烛图数据
  data.forEach(item => {
    arr.push([
      item.open,
      item.high,
      item.low,
      item.close
    ])
    rsiArr.push(item.RSI)
    lbArr.push(item.LB)
  });
  return {
    data: arr,
    RSI: rsiArr,
    LB: lbArr
  }

}
// 计算ma指数
function calculateMA(dayCount) {
  var result = [];
  for (var i = 0, len = data0.values.length; i < len; i++) {
    if (i < dayCount) {
      result.push('-');
      continue;
    }
    var sum = 0;
    for (var j = 0; j < dayCount; j++) {
      sum += +data0.values[i - j][1];
    }
    result.push(sum / dayCount);
  }
  return result;
}

